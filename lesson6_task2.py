# Добавьте в пакет, созданный на семинаре шахматный модуль. Внутри него напишите код, решающий задачу о 8 ферзях. Известно, что на доске 8×8 можно расставить 8 ферзей так, чтобы они не били друг друга. Вам дана расстановка 8 ферзей на доске, определите, есть ли среди них пара бьющих друг друга. Программа получает на вход восемь пар чисел, каждое число от 1 до 8 - координаты 8 ферзей. Если ферзи не бьют друг друга верните истину, а если бьют - ложь.

def is_safe_queens():
    queens = []
    
    for i in range(8):
        while True:
            try:
                x, y = map(int, input(f"Введите координаты {i + 1}-го ферзя (два числа от 1 до 8 через пробел): ").split())
                if 1 <= x <= 8 and 1 <= y <= 8:
                    queens.append((x, y))
                    break
                else:
                    print("Координаты должны быть от 1 до 8. Попробуйте снова.")
            except ValueError:
                print("Некорректный ввод. Введите два числа через пробел.")
    
    for i in range(8):
        for j in range(i + 1, 8):
            x1, y1 = queens[i]
            x2, y2 = queens[j]
            
            if x1 == x2 or y1 == y2 or abs(x1 - x2) == abs(y1 - y2):
                return False  # Ферзи бьют друг друга
    
    return True  # Ферзи не бьют друг друга

# Запуск функции
print(is_safe_queens())
